def input_hex(prompt, length=None):
    while True:
        val = input(prompt).strip().replace(" ", "")
        try:
            b = bytes.fromhex(val)
            if length and len(b) != length: raise ValueError
            return b
        except: 
            msg = f" ({length} байт)" if length else ""
            print(f"  [!] Ошибка: ожидается HEX{msg}")

