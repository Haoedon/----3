# ---------- ЛОГИКА AES ----------
def aes_sub_bytes(state, inv=False):
    box = INV_SBOX_AES if inv else SBOX_AES
    for i in range(16): state[i] = box[state[i]]

def aes_shift_rows(s):
    s[1], s[5], s[9], s[13] = s[5], s[9], s[13], s[1]
    s[2], s[6], s[10], s[14] = s[10], s[14], s[2], s[6]
    s[3], s[7], s[11], s[15] = s[15], s[3], s[7], s[11]

def aes_inv_shift_rows(s):
    s[1], s[5], s[9], s[13] = s[13], s[1], s[5], s[9]
    s[2], s[6], s[10], s[14] = s[6], s[10], s[14], s[2]
    s[3], s[7], s[11], s[15] = s[7], s[11], s[15], s[3]

def xtime(a): return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1) & 0xFF

def mix_col(a):
    t = a[0] ^ a[1] ^ a[2] ^ a[3]
    u = a[0]
    a[0] ^= t ^ xtime(a[0] ^ a[1]); a[1] ^= t ^ xtime(a[1] ^ a[2])
    a[2] ^= t ^ xtime(a[2] ^ a[3]); a[3] ^= t ^ xtime(a[3] ^ u)

def aes_mix_columns(s):
    for i in range(0, 16, 4):
        col = s[i:i+4]; mix_col(col); s[i:i+4] = col

def aes_key_exp(key):
    w = list(key)
    for i in range(4, 44):
        temp = w[(i-1)*4 : i*4]
        if i % 4 == 0:
            temp = [SBOX_AES[b] for b in temp[1:] + temp[:1]]
            temp[0] ^= RCON[i//4]
        for j in range(4): w.append(w[(i-4)*4 + j] ^ temp[j])
    return [w[i*16:(i+1)*16] for i in range(11)]

def aes_process_block(block, key, encrypt=True):
    state = list(block); keys = aes_key_exp(key)
    if encrypt:
        for i in range(16): state[i] ^= keys[0][i]
        for i in range(1, 10):
            aes_sub_bytes(state); aes_shift_rows(state); aes_mix_columns(state)
            for j in range(16): state[j] ^= keys[i][j]
        aes_sub_bytes(state); aes_shift_rows(state)
        for j in range(16): state[j] ^= keys[10][j]
    else:
        for j in range(16): state[j] ^= keys[10][j]
        for i in range(9, 0, -1):
            aes_inv_shift_rows(state); aes_sub_bytes(state, True)
            for j in range(16): state[j] ^= keys[i][j]
            # Упрощенная инверсия MixColumns для примера
            for _ in range(3): aes_mix_columns(state) 
        aes_inv_shift_rows(state); aes_sub_bytes(state, True)
        for j in range(16): state[j] ^= keys[0][j]
    return bytes(state)

# ---------- ЛОГИКА МАГМА ----------
def magma_g(k, a):
    res = 0
    for i in range(8):
        res |= PI_MAGMA[i][((a + k) >> (4 * i)) & 0xF] << (4 * i)
    return ((res << 11) | (res >> 21)) & 0xFFFFFFFF

def magma_process(data, key_bytes, encrypt=True):
    keys = list(struct.unpack('>8I', key_bytes))
    round_keys = (keys * 3 + keys[::-1]) if encrypt else (keys + keys[::-1] * 3)
    out = bytearray()
    for i in range(0, len(data), 8):
        a1, a0 = struct.unpack('>II', data[i:i+8])
        for r in range(31): a1, a0 = a0, magma_g(round_keys[r], a0) ^ a1
        a1 = magma_g(round_keys[31], a0) ^ a1
        out.extend(struct.pack('>II', a1, a0))
    return bytes(out)

# ---------- ЛОГИКА КУЗНЕЧИК ----------
def kuz_s(state):
    for i in range(16): state[i] = PI_KUZ[state[i]]

def kuz_l(state):
    # Упрощенная L-сеть на основе XOR для демонстрации (в реальном ГОСТ используются коэффициенты GF(2^8))
    for _ in range(16):
        res = 0
        for b in state: res ^= b
        state[:] = state[1:] + [res]

def kuz_process(data, key_bytes, encrypt=True):
    # Заглушка процесса Кузнечика для примера (X-S-L раунды)
    state = bytearray(data[:16])
    key = key_bytes[:16]
    for _ in range(10):
        for i in range(16): state[i] ^= key[i] # X
        if encrypt:
            kuz_s(state) # S
            kuz_l(state) # L
        else:
            # Для расшифрования нужны инверсные S и L
            pass 
    return bytes(state)

